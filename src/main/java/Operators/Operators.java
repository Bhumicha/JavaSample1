package Operators;

public class Operators {
    public static void main (String[] args){
        //arithmetic operators +-*/ %
        int a=20, b=10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(b/a);
        System.out.println(b%a);

        //relational/comparison operators
        System.out.println(a>b);
        System.out.println(a<=b);
        a=20;
        System.out.println(a>=b);
        System.out.println(a!=b);

    }
}
